<?php
namespace backend\controllers;

use Yii;
use yii\web\Controller;
use yii\filters\VerbFilter;
use yii\filters\AccessControl;
use common\models\LoginForm;
use yii\data\ActiveDataProvider;

/**
 * Site controller
 */
class SiteController extends Controller
{
    /**
     * @inheritdoc
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'rules' => [
                    [
                        'actions' => ['login', 'error'],
                        'allow' => true,
                    ],
                    [
                        'actions' => ['logout', 'index'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * @inheritdoc
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
        ];
    }

    public function actionIndex()
    {
         $dataProviderArtikel = new ActiveDataProvider([
            'query' => \common\models\ArticleItems::find()
                    ->where ('catid != :ArticleCategories', [
                        ':ArticleCategories' => isset($_POST['ArticleCategories']) ? $_POST['ArticleCategories'] : "6" & "9"
                        ]),

            'sort' => [
                    'defaultOrder' => [
                        'id' => SORT_DESC
                    ]
            ]
            ]);

        return $this->render('index', [
            'dataProviderArtikel' => $dataProviderArtikel
            ]);
    }

    public function actionView($id)
    {
        $model = \common\models\ArticleItems::findOne($id);

             

        return $this->render('view', [
            'model' => $model,
        ]);
    }

    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        } else {
            return $this->render('login', [
                'model' => $model,
            ]);
        }
    }

    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }
}
